// var: 같은 변수에 여러번 할당 가능
var greeting = "hello";
console.log(greeting);

var greeting = "hi";
console.log(greeting);

greeting = "how are you?";
console.log(greeting);

// let: 중복 선언불가, 재할당 가능
let greeting2 = "hello";
console.log(greeting2);

greeting2 = "hi";
console.log(greeting2);

// const 중복 선언 x, 재할당 x
const greeting3 = "hello";
console.log(greeting3);

